package com.tnsif.daytwo;

public class ForEach {

	public static void main(String[] args) {
		int a[]= {10,20,30,50,60};
		
		for(int i:a)
		{
			System.out.println(i);
		}
		
		char ch[]= {'j','a','v','a'};
		
		for(char c:ch)
		{
			System.out.println(c);

		}
		
		
		
	}

}
